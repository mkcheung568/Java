    private void jButton_SaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton_SaveActionPerformed
        // TODO add your handling code here:
        String filePath = "G33m4CHEUNG.csv";
        File file = new File(filePath);
        
	int op =JOptionPane.showConfirmDialog(null,"Do you want to apply this appointment?","Select an Option...",JOptionPane.YES_NO_OPTION);


	if(op==0){
            try {
            FileWriter fw = new FileWriter(file);
            BufferedWriter bw = new BufferedWriter(fw);
            
            for(int i = 0; i < table.getRowCount(); i++){//rows
                for(int j = 0; j < table.getColumnCount(); j++){//columns
                    bw.write(table.getValueAt(i, j).toString()+" ");
                }
                bw.newLine();
            }
            
            bw.close();
            fw.close();
            
        } catch (IOException ex) {
            
        }
        JOptionPane.showMessageDialog(null, "Appointment Saved");
        new G33m4Course_App().setVisible(true);
        super.dispose();
        }        

    }//GEN-LAST:event_jButton_SaveActionPerformed

    private void jButton_CheckActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton_CheckActionPerformed
        // TODO add your handling code here:
        String filePath = "G33m4CHEUNG.csv";
        File file = new File(filePath);
        
        try {
            FileReader fr = new FileReader(file);
            BufferedReader br = new BufferedReader(fr);
            
            DefaultTableModel model = (DefaultTableModel)table.getModel();
            Object[] lines = br.lines().toArray();
            
            for(int i = 0; i < lines.length; i++){
                String[] row = lines[i].toString().split(" ");
                model.addRow(row);
            }
           
        } catch (FileNotFoundException e) {
            
        }
        jButton_Check.setVisible(false);
    }//GEN-LAST:event_jButton_CheckActionPerformed

