    private void jButton_EditActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton_EditActionPerformed
        // TODO add your handling code here:
        //User ID
        String UserID = jTextField_UID.getText();
        
        //Reserved Time
        String CT = (String) jComboBox_Time.getSelectedItem();
        //Recorded Time
        DateFormat dateFormat = new SimpleDateFormat("MM/dd/yy_HH:mm:ss");
        Date Rtime = new Date();
        
        //Remarks
        String RM = jTextField_Rm.getText();

        int i = table.getSelectedRow();
        if(i >= 0) 
        {
           model.setValueAt("AR04"+ARN, i, 0);
           model.setValueAt(UserID, i, 1);
           model.setValueAt(CT, i, 2);
           model.setValueAt("Economic", i, 3);
           model.setValueAt(dateFormat.format(Rtime), i, 4);
           model.setValueAt(RM, i, 5);
        }
    }//GEN-LAST:event_jButton_EditActionPerformed

